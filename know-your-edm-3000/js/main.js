var resetDOM = function () {
  // Track info Reset
  elmTrackInfo.target = "_self"
  elmTrackInfo.href = '#'
  // Pads Reset
  for (var i = elmOptions.length - 1; i >= 0; i--) {
    elmOptions[i].classList.remove('correct')
    elmOptions[i].classList.remove('wrong')
    elmOptions[i].classList.remove('selected')
  }
}

var renderAnswersCorrectness = function (clickedElm) {
  // Show track information
  updateDisplayText(currentRoundData.correctTrack.title)
  // Make it open in new tab
  elmTrackInfo.target = "_blank"
  // Set youtube link for current track
  elmTrackInfo.href = 'https://www.youtube.com/watch?v=' + currentRoundData.correctTrack.ytId
  // Mark the correct answer
  setTimeout(function () {
    for (var i = elmOptions.length - 1; i >= 0; i--) {
      if (elmOptions[i].dataset.genre === currentRoundData.correctTrack.genre) {
          let currentElm = elmOptions[i]
          updateScore()
          // If picked answer is wrong, wait abit to show correct
          if(clickedElm != elmOptions[i]){
            clickedElm.classList.remove('selected')
            clickedElm.classList.add('wrong')
            setTimeout(function () {
              currentElm.classList.add('correct')
            }, 500)
          } else {
            currentElm.classList.add('correct')
          }

      }
    }
  }, 800)


}

var renderGameBoard = function (roundData) {
  var boardData = roundData.roundGenres
  for (let index in boardData) {
    // Render names in option boxes
    elmOptions[index].getElementsByTagName('h3')[0].innerHTML = boardData[index]
    // Set genre as data attribute
    elmOptions[index].dataset.genre = boardData[index]
  }
}

var disablePads = function (disable) {
  if(disable){
    elmOptionsParent.classList.add('disable-clicks')
  } else {
    elmOptionsParent.classList.remove('disable-clicks')
  }
}

var isLoading = function (loading) {
  if(loading){
    updateDisplayText('LOADING...')
    elmTrackInfo.classList.add('blink')
  } else {
    updateDisplayText('????? - ?????') 
    elmTrackInfo.classList.remove('blink')
  }
}

var muteToggle = function () {
 if(musicPlayer.isMuted()){
    musicPlayer.unMute()
    elmMute.classList.remove('active')
  } else{
    musicPlayer.mute()
    elmMute.classList.add('active')
  }
}

updateDisplayText = function (text) {
  elmTrackInfo.innerHTML = text
}

var startGame = function () {
    elmStart.classList.remove('active')
    elmStart.innerHTML = 'RESTART'
    startNewGameRound()
    score.currentScore = 000
    score.totalRounds = 000
    updateScore()
}

var initPads = function () {
  for (var i = elmOptions.length - 1; i >= 0; i--) {
    elmOptions[i].addEventListener('click', function () {
      checkAnswer(this)
    }, false)
  }
}

