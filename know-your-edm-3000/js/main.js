var currentRoundData

var score = {
  currentScore: 000,
  totalRounds: 000
}

function padToThree(number) {
  if (number<=999) { number = ("00"+number).slice(-3); }
  return number;
}

var startNewGameRound = function () {
  // Reset dom to default state
  resetDOM()
  // Create data for a new round
  drawNewRound().then(function (roundData) {
    // Load video
    musicPlayer.loadVideoById(roundData.correctTrack.ytId, 40)
    // Make roundData avalible out of scope
    currentRoundData = roundData
    // Render roundData in DOM
    renderGameBoard(roundData)
  })
}

var drawNewRound = function () {
  return new Promise(function (resolve, reject) {
    // Creating the basci object
    var roundData = {
      'correctTrack': null,
      'roundGenres': []
    }
    // Draw Correct answer
    var track = _.sample(gameData.tracks)
    roundData.correctTrack = track
    roundData.roundGenres.push(track.genre)
    // Add uniq Decoy Genres
    var drawUniqDecoyGenre = function () {
      var drawedGenre = _.sample(gameData.genres)
      if (~roundData.roundGenres.indexOf(drawedGenre)) {
      // if genre already exists draw again
        drawUniqDecoyGenre()
      } else {
        // add new genre to array
        roundData.roundGenres.push(drawedGenre)
        // resolve when we have 4 uniq genres else draw a new one
        if (roundData.roundGenres.length === 4) {
          // Shuffel genres befor resolving
          roundData.roundGenres = _.shuffle(roundData.roundGenres)
          resolve(roundData)
        } else {
          drawUniqDecoyGenre()
        }
      }
    }
    drawUniqDecoyGenre()
  })
}

var checkAnswer = function (elm) {
  disablePads(true)
  elm.classList.add('selected')
  var clickedGenre = elm.dataset.genre
  var correctGenre = currentRoundData.correctTrack.genre
  // Is answer Correct?
  clickedGenre === correctGenre ? correctAnswer() : wrongAnswer()
  score.totalRounds++
  renderAnswersCorrectness(elm)
  setTimeout(startNewGameRound, 3500)
}

