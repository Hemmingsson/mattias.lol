var musicPlayer
var musicPlayerIsReady = false


var onYouTubeIframeAPIReady = function () {
  musicPlayer = new YT.Player('musicPlayer', {
    events: {
      'onReady': onPlayerReady,
      'onStateChange': onPlayerStateChange,
      'onError': onPlayerError
    }
  })
}

var onPlayerReady = function () {
  console.log('Player Ready!')
  musicPlayerIsReady = true
}

var onPlayerStateChange = function (e) {
  if (e.data == -1) {
    // unstarted
  } else if (e.data == 0) {
    // ended
  } else if (e.data == 1) {
    // playing
  } else if (e.data == 2) {
    // paused
  } else if (e.data == 3) {
    // buffering
  } else if (e.data == 5) {
    // video cued
  }

  if (e.data == 3) {
    isLoading(true)
  } else {
    isLoading(false)
  }

  // If video is playing
  if (e.data == 1) {
    disablePads(false)
    elmEqualizer.classList.add('animate')
  } else {
    elmEqualizer.classList.remove('animate')
  }
}

var onPlayerError = function (e) {
  startNewGameRound()
}

